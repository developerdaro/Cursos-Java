package ejercicios;


public class NumerosIntervaloSiete {
    
    public static void main(String[] args) {
        
        /*
        ENUNCIADO
        
        Escribir todos los numeros del 100 al 0 de 7 en 7.
        */
        
        for (int i=0;i<=100;i=i+7) {
            System.out.println(i);
        }
        
    }
    
}
